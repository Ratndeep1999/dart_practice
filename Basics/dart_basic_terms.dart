// Programing : process of telling computer that which task has to perform in order to solve the problem.

// Coding : set of instructions that read by computer in binary codes (0 and 1).

// Statement : commands that tell computer to do something,
// it is a lines of codes which is end with semi colon.

// Keywords : reserved words that gives special meaning to the compiler.

// Expression : represents the value or something that is count as value,
// like int, string etc.

// Identifiers : it is the given names by programmer to define variables, methods, classes etc.
// it must be unique not same as the keywords.

// High-level-lang : Language that is easy to understand and user-friendly,
// has english like sentences and use to develop software.

// Low-level-language : Language that is hard to learn and not-user-friendly,
// deals with computer hardware parts and faster than high level language.

// Compiler : Computer program that translate high-level-language into machine-level-language.

// Syntax : It is kind of format or rule that gives concept to write the code.
// it is like blue-prints to write codes.

// Compile-time : phase when we write the codes before the execution.

// Run-Time : phase when we executing the program

// Difference between compile-time and Run-time :

// Compile-time : phase when dart check the codes before execution.
// type and syntax error occur at compile-time called as compile time error.

// Run-time : phase when the program executing.
// logical and exceptions occur at run-time known as run-time error.

// variable : It is the given name to the particular location where we store some data.
// it is like container, inside that we store type of content.

// Rules
// It can contain alphabet and numbers (alphaNumeric)
// Must be start with Alphabet or "_"
// Space is not allowed
// Case sensitive
// Only two special char are allowed '$' and '_'
// Not to same as Keywords. must be unique

// Type-annotation
// <dataType> <variableName>

// Data-type : use to define what the type of data we are assigning to the variable.

// Scope : it defines the accessible area of the program.
// There are two types of Scopes Local and Global.

// Local Scope : variable that define inside particular function or block of the code, and is accessible only
// inside that function or block of code is known as Local Scope.
// Scope where variable can be define indide function or block of code is only accessible inside that area.

// Global Scope : variable that define outside any function or block is accessible anywhere in the program
// or project.



